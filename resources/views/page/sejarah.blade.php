<x-layout.post title="Sejarah LPMPP">
    <p>Lembaga Pengembangan Pembelajaran dan Penjaminan Mutu Pendidikan (LP3MP) dibentuk berdasarkan Peraturan Menteri RISTEKDIKTI Nomor 20 Tahun 2016 tentang Organisasi dan Tata Kerja Universitas Pattimura. LP3MP UNPATTI didirikan pada Tanggal 07 Februari 2017 Sesuai Surat Keputusan Rektor No : 182/UN13/SK/2017. LP3MP bertugas merencanakan, melaksanakan, mengkoordinasi, memantau dan mengevaluasi kegiatan pengembangan pembelajaran dan penjaminan mutu.</p>
    <p>Dalam menjalankan tugas sebagaimana dimaksud di atas, LP3MP menyelenggarakan fungsi:</p>
    <ul>
        <li>Penyusunan rencana, program dan anggaran Lembaga;</li>
        <li>Pelaksanaan pengembangan sistem pembelajaran;</li>
        <li>Pelaksanaan peningkatan mutu pembelajaran;</li>
        <li>Pelaksanaan pengembangan sistem penjaminan mutu pendidikan;</li>
        <li>Pelaksanaan penjaminan mutu pendidikan;</li>
        <li>Koordinasi pelaksanaan kegiatan pengembangan pembelajaran dan penjaminan mutu pendidikan;</li>
        <li>Pelaksanaan fasilitasi peningkatan mutu proses pembelajaran;</li>
        <li>Pemantauan dan evaluasi pengembangan pembelajaran dan penjaminan mutu pendidikan;</li>
        <li>Pelaksanaan perbaikan proses pembelajaran;</li>
        <li>Pelaksanaan urusan administrasi Lembaga;</li>
        <li>Pelaksanaan kegiatan pengembangan Tenaga Kependidikan; dan</li>
        <li>Pelaksanaan kegiatan pengembangan Karakter.</li>
    </ul>
