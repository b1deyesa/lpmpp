    <h5>Visi</h5>
    <ul>
        <li>Mewujudkan UNPATTI Bermutu</li>
    </ul>

    <h5>Misi</h5>
    <ul>
        <li>Menerapkan dan mengembangkan Sistem Penjaminan Mutu Internal yang sesuai perkembangan dan sosial-budaya kampus;</li>
        <li>Meningkatkan kualitas sumberdaya manusia pelaksana Sistem Penjaminan Mutu Internal sesuai Standar Nasional Pendidikan Tinggi;</li>
        <li>Meningkatkan dan mengembangkan Sistem Informasi Penjaminan Mutu berbasis ICT;</li>
        <li>Melaksanakan dan mengintensifkan Audit Mutu Internal, monitoring dan evaluasi sesuai Siklus Jaminan Mutu;</li>
        <li>Meningkatkan kualitas dan relevansi Program Studi dan Institusi melalui pendampingan peningkatan Akreditasi secara Nasional dan Internasional.</li>
    </ul>

    <h5>Tujuan</h5>
    <ul>
        <li>Memprioritaskan penjaminan mutu untuk keberlanjutan Universitas Pattimura</li>
        <li>Meningkatkan mutu standar SPMI</li>
        <li>Meningkatkan mutu pembelajaran</li>
        <li>Mengembangkan sistem penjaminan mutu akademik</li>
        <li>Mewujudkan nilai akreditasi akademik Institusi dan Program Studi</li>
    </ul>

